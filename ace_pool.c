#include "ace_thread.h"
#include "ace_pool.h"

int check_flag(pool_t *pool, const u32 flag)
{
	int rc = FALSE;

	mutex_lock(&pool->flag_mutex);
	rc = (pool->flags & flag);
	mutex_unlock(&pool->flag_mutex);

	return rc;
}

